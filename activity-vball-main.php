			<div id="rules">
				<h3>Team Volleyball Rules</h3>

				<p>We&#39;re highlighting just a few rules and procedures for the team volleyball games. Standard volleyball rules apply.</p>

				<ul>
					<li><b>COIN TOSS:</b> To start each match, the referee will toss a coin to determine who serves first.
					The team captain who wins the first toss will get to choose either to serve first or which side to
					play on. Whoever starts serving in the first game will not start serving in the second game. The
					referee will also toss a coin to determine who serves first in the third game.</li>

					<li><b>SERVING:</b> You can serve anywhere behind the line.</li>

					<ul>
						<li><u>Foot Fault:</u> If your foot touches the line while you serve, that is an automatic
						loss of serve.</li>

						<li><u>Let serve:</u> The ball CAN touch the net on the serve and play continues.</li>

						<li><u>Attacking a serve:</u> Blocking or spiking a serve is illegal.

					</ul>

					<li><b>SCORING:</b> We will play rally point in all games. The first two games of each match
					will be to 25 and the third game will be to 15. All games will also be on a time limit. A team
					must win by two points, even if the score is tied when time runs out.</li>

					<li><b>PASSING:</b> Players may contact the ball with any part of the body, INCLUDING the feet,
					whether intentionally or not.</li>

					<li><b>LIFT:</b> The palm of a player&#39;s hand cannot touch the volleyball while setting. The ball
					cannot come to rest in a player&#39;s arms or hand.</li>

					<li><b>DOUBLE HIT:</b> A player cannot touch a ball twice, with the exception of a block and
					then a hit by the same player. This includes the ball rolling along a player&#39;s body or arm except
					when a player is receiving the serve.</li>

					<li><b>SUBSTITUTION:</b> If you are planning to have a player sub in, notify the ref before
					play begins that you intend to do so. A substitute may not play the same position twice before
					his/her team has rotated 5 times.</li>

					<li><b>10-FOOT LINE:</b> A back-row player may not jump in front of the 10-foot line to spike
					a ball. A back-row player may attack a ball in front of the 10-foot line, if at the moment of
					contact the ball is not entirely higher than the top of the net.</li>

					<li><b>ROTATION:</b> Teams must maintain the same rotation per game as at the first serve.</li>

					<li><b>FORFEIT:</b> All games will start promptly at the time listed. If your team is not on
					the court on time, you will forfeit the first game. If your team is not on the court 5 minutes
					after the first game is scheduled to begin, you will forfeit the second game of the match. If
					your team is not on the court 10 minutes after the first game of the match is scheduled to begin,
					you will forfeit all three games of the match. All points of each forfeited game will
					automatically go to the opposing team. A minimum of four (4) players per team are required to start a match.</li>

					<li><b>MISCELLANEOUS:</b> During play, it is illegal for any part of a player&#39;s body to touch the net.

				</ul>


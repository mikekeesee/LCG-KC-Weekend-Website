		$(function() {
			$('.daily-schedule').hide();
			
			// Start showing the divs
			showDiv();
		});

		var divCnt = 0;
		
		function showDiv() {				
			$('.daily-schedule').each(function(index) {
				if (index == divCnt) {
					$(this).fadeIn();
				} else {
					$(this).hide();
				}
			});
			
			divCnt = (divCnt + 1) % 3;
			
			// And wait one second before fading in the next one
			setTimeout(showDiv, 5000);
		}
	</script>	
